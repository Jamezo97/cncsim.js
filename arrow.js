var VertexNormalModel = function(data)
{
    this.triangleCount = data.length / 6;
    
    this.buffer = null;
    
    this.genBuffers = function(gl)
    {
        this.buffer = gl.createBuffer();
        gl.bindBuffer(gl.ARRAY_BUFFER, this.buffer);
        gl.bufferData(gl.ARRAY_BUFFER, data, gl.STATIC_DRAW);
    }
    
    this.deleteBuffers = function(gl)
    {
        gl.deleteBuffer(this.buffer);
        this.buffer = null;
    }
    
    this.render = function(gl, vertexLoc, normalLoc)
    {        
        gl.bindBuffer(gl.ARRAY_BUFFER, this.buffer);
        gl.vertexAttribPointer(vertexLoc, 3, gl.FLOAT, false, 24, 0);
        gl.vertexAttribPointer(normalLoc, 3, gl.FLOAT, false, 24, 12);
        gl.enableVertexAttribArray(vertexLoc);
        gl.enableVertexAttribArray(normalLoc);
        
        
        gl.drawArrays(gl.TRIANGLES, 0, this.triangleCount);  
    }
}