var CylinderModel = function()
{
    
    this.triangleCount = cylinderData.length / 6;
    
    this.buffer = null;
    
    this.genBuffers = function(gl)
    {
        this.buffer = gl.createBuffer();
        gl.bindBuffer(gl.ARRAY_BUFFER, this.buffer);
        gl.bufferData(gl.ARRAY_BUFFER, cylinderData, gl.STATIC_DRAW);
    }
    
    this.deleteBuffers = function(gl)
    {
        gl.deleteBuffer(this.buffer);
        this.buffer = null;
    }
    
    this.render = function(gl, vertexLoc, normalLoc)
    {
        //console.log(vertexLoc);
        //console.log(normalLoc);
        
        gl.bindBuffer(gl.ARRAY_BUFFER, this.buffer);
        gl.vertexAttribPointer(vertexLoc, 3, gl.FLOAT, false, 24, 0);
        gl.vertexAttribPointer(normalLoc, 3, gl.FLOAT, false, 24, 12);
        gl.enableVertexAttribArray(vertexLoc);
        gl.enableVertexAttribArray(normalLoc);
        
        
        gl.drawArrays(gl.TRIANGLES, 0, this.triangleCount);  
    }
}

function CylinderShader()
{
    var me = this;

    {
        var vertexShader = getShader(gl, "2d-vertex-shader-cylinder");
        var fragmentShader = getShader(gl, "2d-fragment-shader-cylinder");

        this.program = gl.createProgram();

        gl.attachShader(this.program, vertexShader);
        gl.attachShader(this.program, fragmentShader);
        gl.linkProgram(this.program);

        gl.deleteShader(vertexShader);
        gl.deleteShader(fragmentShader);

        if (!gl.getProgramParameter(this.program, gl.LINK_STATUS))
        {
            // An error occurred while linking
            document.getElementById('heading').innerHTML += "WebGL - Shader Initialization Error";
            document.getElementById('info').innerHTML += "WebGL could not initialize one, or both, shaders.";
            gl.deleteProgram(program);
            return;
        }

        gl.useProgram(this.program);
    }

    this.modelMatrix_loc = gl.getUniformLocation(this.program, 'modelMatrix');
    this.viewMatrix_loc = gl.getUniformLocation(this.program, 'viewMatrix');
    this.projMatrix_loc = gl.getUniformLocation(this.program, 'projMatrix');
    this.colour_loc = gl.getUniformLocation(this.program, 'colour');

    this.vertexAttributeLoc = gl.getAttribLocation(this.program, "a_position");
    this.normalAttributeLoc = gl.getAttribLocation(this.program, "a_normal");

    this.loadGLTMatrices = function(glt)
    {
        this.useShader();
        gl.uniformMatrix4fv(this.modelMatrix_loc, false, glt.modelMatrix.data);
        gl.uniformMatrix4fv(this.viewMatrix_loc, false, glt.viewMatrix.data);
        gl.uniformMatrix4fv(this.projMatrix_loc, false, glt.projectionMatrix.data);
    }
    
    this.setColour = function(rgb)
    {
        this.useShader();
        gl.uniform3fv(this.colour_loc, new Float32Array([rgb[0], rgb[1], rgb[2]]));
    }

    this.useShader = function()
    {
        gl.useProgram(this.program);
    }

}